export class PlayerTeamInfo {
  constructor(
    id,
    club,
    section,
    age_category,
    team_number,
    city,
    country,
    zipcode
  ) {
    this.id = id;
    this.club = club;
    this.section = section;
    this.age_category = age_category;
    this.team_number = team_number;
    this.city = city;
    this.country = country;
    this.zipcode = zipcode;
  }
}
