export class UserProfileDetails {
  constructor(
    first_name,
    last_name,
    avatar,
    gender,
    nick_name,
    email,
    mobile_num,
    birth_date,
    nationality
  ) {
    this.first_name = first_name;
    this.last_name = last_name;
    this.avatar = avatar;
    this.gender = gender;
    this.nick_name = nick_name;
    this.email = email;
    this.mobile_num = mobile_num;
    this.birth_date = birth_date;
    this.nationality = nationality;
  }
}
